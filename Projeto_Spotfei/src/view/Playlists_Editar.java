    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        // TODO add your handling code here:
        Buscar b = new Buscar(this);
        int escolha = c.opcoes();
        if(escolha == 0){
            DefaultListModel<String> model = new DefaultListModel<>();
            model.removeAllElements();
            b.getLista_musicas().setModel(model);
            b.getLista_musicas().setFixedCellHeight(30);
            b.setVisible(true);
            b.getLbl_info().setVisible(false);
            this.setVisible(false);
        }
        
        else if(escolha == 1){
            Playlists_Ver pv = new Playlists_Ver(this);
            c.mostrarPlaylists(pv.getLista_playlists());
            pv.setVisible(true);
        }
        
    }//GEN-LAST:event_btn_editarActionPerformed

